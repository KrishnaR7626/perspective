use perspective_client::*;
use perspective_server::*;
pub use {perspective_client as client, perspective_server as server};

pub async fn create_local_client(server: &Server) -> ClientResult<Client> {
    let server = server.clone();
    let client = Client::new({
        move |client, req| {
            let server = server.clone();
            let client = client.clone();
            let req = req.clone();
            async move {
                for (_client_id, resp) in server.handle_request(0, &req) {
                    client
                        .handle_response(&resp)
                        .await
                        .unwrap_or_else(|e| tracing::error!("Unknown server message: {}", e));
                }

                for (_client_id, resp) in server.poll() {
                    client
                        .handle_response(&resp)
                        .await
                        .unwrap_or_else(|e| tracing::error!("Unknown server message: {}", e));
                }
            }
        }
    });

    client.init().await?;
    Ok(client)
}
